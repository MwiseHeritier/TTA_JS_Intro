// Arithematic operators
let a =10;
let b =5;
let result = a + b;
console.log(result); //15   

// compound Assignment operator
let x = 10;
x += 5;
console.log(x); //15

// comparison operator
let isEqual = (a==b);
console.log(isEqual); //false

// logical operator
let logicalResult = (a > 5 && b < 10);
console.log(logicalResult); //true

// string operator
let fullName = "John" + " " + "Doe"
console.log(fullName); //John Doe

// Ternary operator
let max = (a > b) ? a : b;
console.log(max); //10
